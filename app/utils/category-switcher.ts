// Helper function to generate random weights between min and max (exclusive)
const randomWeight = (min = 0, max = 1) => {
  return Number((Math.random() * (max - min) + min).toFixed(4));
};

// Generate unique weights for opt categories
const generateWeights = (count: number): number[] => {
  const weights = new Set<number>();
  while (weights.size < count) {
    weights.add(randomWeight(0, 1));
  }
  return Array.from(weights);
};

